type IProps = {
  chartData: ChartData<"line", (number | Point | null)[], unknown>,
  chartTitle: string
}

const LineChart: React.FC<IProps> = ({ chartData, chartTitle }) => {
  const options = {
    responsive: true,
    plugins: {
      legend: {
        position: 'top' as const,
      },
      title: {
        display: true,
        text: chartTitle,
      },
    },
  };

  return <Line options={options} data={chartData} />;
}

export default LineChart